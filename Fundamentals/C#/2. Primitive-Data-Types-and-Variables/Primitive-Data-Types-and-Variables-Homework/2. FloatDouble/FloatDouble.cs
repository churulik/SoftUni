using System;

class FloatDouble
{
    static void Main()
    {
        double numOne = 34.56783923;
        float numTwo = 12.345F;
        double numThree = 8923.1234857;
        float numFour = 3456.091F;
        Console.WriteLine("{0}, {1}, {2}, {3}", 
            numOne, numTwo, numThree, numFour);
    }
}

